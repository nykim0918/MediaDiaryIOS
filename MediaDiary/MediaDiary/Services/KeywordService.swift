    func keywords(for type: String, genre: String? = nil) -> [String] {
        guard let typeMap = keywordMap[type] else { return [] }
        var result = typeMap["base"] ?? []
        if let genre = genre, let genreKeywords = typeMap[genre] {
            result += genreKeywords
        }
        return result
    }

    func allKeywords(for type: String) -> [String] {
        guard let typeMap = keywordMap[type] else { return [] }
        var result: [String] = []
        for (_, words) in typeMap {
            result += words
        }
        return Array(Set(result)).sorted()
    }

    func suggestKeywords(for type: String, genre: String?, input: String) -> [String] {
        let all = keywords(for: type, genre: genre)
        if input.isEmpty { return all }
        return all.filter { $0.localizedCaseInsensitiveContains(input) }
    }
